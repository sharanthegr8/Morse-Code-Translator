def morseToText(morse):
    words = morse.split(' / ')
    message = ""
    
    for word in words:
        chars = word.strip().split()
        for char in chars:
            message += Text.get(char, '<?>')
        message += " "

    return message


def frequencyAnalysis(morse):
    codes = morse.split()
    counter = {}
    for i in codes:
        if i not in counter:
            counter[i] = 1
        else:
            counter[i] += 1
 
    Scounter = dict(sorted(counter.items(), key=lambda item: item[1], reverse=True))
    return Scounter


def textToMorse(text):
 
    text = text.upper()
    morse = ""
    for i in text:
        if i in morseCode:
            morse += morseCode[i]
        elif i == '\n' or i == '\t':
            morse += morseCode.get(' ', '/ ')  # treat as space
        else:
            morse += '<?> '
    return morse
 